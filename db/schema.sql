-- Meal Planner — Postgres schema (portfolio / AWS RDS)
-- Surrogate keys: INTEGER + IDENTITY (~2B rows max per table; fine for a small app).
-- Extensions for full-text recipe search
CREATE EXTENSION IF NOT EXISTS pg_trgm;

-- ---------------------------------------------------------------------------
-- Users: map to Cognito `sub` in application layer
-- ---------------------------------------------------------------------------
CREATE TABLE users (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  cognito_sub TEXT UNIQUE NOT NULL,
  email TEXT,
  display_name TEXT,
  preferred_mass_unit TEXT DEFAULT 'g',
  preferred_volume_unit TEXT DEFAULT 'ml',
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- Recipes
-- ---------------------------------------------------------------------------
CREATE TYPE recipe_status AS ENUM ('library', 'planned');

CREATE TABLE recipes (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER NOT NULL REFERENCES users (id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  description TEXT,
  directions TEXT,
  notes TEXT,
  image_url TEXT,
  source_url TEXT,
  category TEXT NOT NULL DEFAULT '',
  keywords TEXT[] NOT NULL DEFAULT '{}',
  prep_time_min INT,
  cook_time_min INT,
  servings NUMERIC NOT NULL DEFAULT 1 CHECK (servings > 0),
  status recipe_status NOT NULL DEFAULT 'library',
  search_vector tsvector,
  ingredients_json JSONB NOT NULL DEFAULT '[]'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX recipes_user_status_idx ON recipes (user_id, status);
CREATE INDEX recipes_search_idx ON recipes USING GIN (search_vector);
CREATE INDEX recipes_keywords_idx ON recipes USING GIN (keywords);

-- ---------------------------------------------------------------------------
-- Ingredient sections ("Batter", "Topping", …)
-- ---------------------------------------------------------------------------
CREATE TABLE ingredient_sections (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  title TEXT NOT NULL,
  position INT NOT NULL DEFAULT 0
);

